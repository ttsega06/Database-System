public class Employee {

    String Employe_ID_number, Employee_name, Address, Gender, Date_of_Employment, Position_Hired;

    public Employee(String employe_ID_number, String employee_name, String address, String gender, String date_of_Employment, String position_Hired) {
        
    }

    public Employee() {

    }

    public String getEmploye_ID_number() {
        return Employe_ID_number;
    }

    public void setEmploye_ID_number(String employe_ID_number) {
        Employe_ID_number = employe_ID_number;
    }

    public String getEmployee_name() {
        return Employee_name;
    }

    public void setEmployee_name(String employee_name) {
        Employee_name = employee_name;
    }

    public String getAddress() {
        return Address;
    }

    public void setAddress(String address) {
        Address = address;
    }

    public String getGender() {
        return Gender;
    }

    public void setGender(String gender) {
        Gender = gender;
    }

    public String getDate_of_Employment() {
        return Date_of_Employment;
    }

    public void setDate_of_Employment(String date_of_Employment) {
        Date_of_Employment = date_of_Employment;
    }

    public String getPosition_Hired() {
        return Position_Hired;
    }

    public void setPosition_Hired(String position_Hired) {
        Position_Hired = position_Hired;
    }
}
